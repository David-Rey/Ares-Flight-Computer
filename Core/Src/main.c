int main(void){

  	HAL_Init();

  	SystemClock_Config();

  	MX_GPIO_Init();
  	MX_I2C2_Init();
  	MX_SPI1_Init();
  	MX_SPI2_Init();
  	MX_USART1_UART_Init();
  	MX_USART2_UART_Init();
  	MX_USB_DEVICE_Init();
  	MX_TIM4_Init();

  	HAL_TIM_IC_Start_IT(&htim4, TIM_CHANNEL_4);

  	BMX160_disable();
  	BMP388_disable();

  	BMX_init();
  	BMP_init();
  	LIS_init();

  	float comp_p, comp_t;
  	char USB_buf[80] = {0};
  	uint32_t counter = 0;

  	BMXrawData acc_raw;
  	BMXrawData gyr_raw;
  	BMXrawData mag_raw;
  	LISrawData lis_acc_raw;

  	while (1) {
  		if (BMXnewDATA){
  			BMX_all_sensor_data(&acc_raw, &gyr_raw, &mag_raw);
  			BMP_temp_press(&comp_t, &comp_p);
  			LIS_acc(&lis_acc_raw);
  			BMXnewDATA = 0;

  			//,gx:%i,gy:%i,gz:%i

  			sprintf(USB_buf, "T:%d,p:%f,t:%f,ax:%i,ay:%i,az:%i,gx:%i,gy:%i,gz:%i \n",
  				counter, comp_p, comp_t,
				acc_raw.x, acc_raw.y, acc_raw.z,
				gyr_raw.x, gyr_raw.y, gyr_raw.z);

  			CDC_Transmit_FS((uint8_t*) USB_buf, sizeof(USB_buf));
  			HAL_UART_Transmit(&huart1, (uint8_t*)USB_buf, sizeof(USB_buf), 10);
  			memset(USB_buf, 0, sizeof(USB_buf));
  			counter++;
  		}
  	}
}

